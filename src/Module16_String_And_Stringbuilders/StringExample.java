package Module16_String_And_Stringbuilders;

public class StringExample {
    public static void main(String[] args) {
        //How to store string
        char[] ch = {'r','a','g','h','a','v'};
        for (int i = 0; i < ch.length; i++) {
            System.out.println(ch[i] + " ");
        }
        //String
        String s ="Birju kumar";
        System.out.println(" "+ s);
    }
}
